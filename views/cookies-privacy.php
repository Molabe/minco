        <h1>Cookies Politika</h1>
        <p>Táto cookies politika vysvetľuje, aké cookies používame na stránke Spognetky.sk a na aký účel. Používanie cookies pomáha zlepšiť vašu používateľskú skúšenosť na našej stránke.</p>

        <h2>1. Čo sú cookies?</h2>
        <p>Cookies sú malé súbory, ktoré sa ukladajú na vašom zariadení pri návšteve našej stránky. Slúžia na ukladanie informácií, ktoré môžu byť použité pre rôzne účely, ako napríklad analýzu návštevnosti alebo prispôsobenie reklamy.</p>

        <h2>2. Aké cookies používame?</h2>
        <p>Na našich stránkach používame tieto typy cookies:</p>
        <ul>
            <li><strong>Nezbytné cookies</strong>: Tieto cookies sú potrebné na správne fungovanie našej stránky a umožňujú jej základné funkcie (napr. zabezpečenie prihlásenia).</li>
            <li><strong>Výkonnostné cookies</strong>: Používame ich na analýzu, ako naši návštevníci používajú stránku. Tieto cookies nám pomáhajú zlepšiť obsah a výkon stránky.</li>
            <li><strong>Funkčné cookies</strong>: Ukladajú vaše preferencie, ako jazykové nastavenia alebo obsah košíka, aby sa vám stránka lepšie prispôsobila.</li>
            <li><strong>Reklamné cookies</strong>: Používame ich na personalizáciu reklám na našich stránkach a na stránkach tretích strán.</li>
        </ul>

