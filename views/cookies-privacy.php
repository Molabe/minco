        <h2>4. Ako zrušiť cookies?</h2>
        <p>Ak chcete zrušiť súhlas s používaním cookies, môžete ich vymazať alebo zablokovať prostredníctvom nastavení svojho prehliadača. Ak sa rozhodnete zablokovať cookies, môže to ovplyvniť funkčnosť našich stránok.</p>

        <h2>5. Právo na odvolanie súhlasu</h2>
        <p>Máte právo kedykoľvek odvolať svoj súhlas so spracovaním údajov. Toto môžete vykonať kliknutím na "Spravovať cookies" na našej stránke alebo prostredníctvom nastavení vášho prehliadača.</p>

